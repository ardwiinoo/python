a = 5
b = 3

# penjumlahan (+)
print('A ditambah B : ' + str(a + b))

# pengurangan (-)
print('A dikurangi B : ' + str(a - b))

# perkalian (*)
print('A dikali B : ' + str(a * b))

# pembagian (hasil rill) (/)
print('A dibagi B : ' + str(a / b))

# pembagian (hasil bulat) (//)
print('A dibagi B : ' + str(a // b))

# modulus (%)
print('A modulus B : ' + str(a % b))

# perpangkatan (**)
print('A pangkat B : ' + str(a**b)) 